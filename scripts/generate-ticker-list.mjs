/**
 * Regenerates src/lib/Top1000Symbols.ts from live Polygon data.
 *
 * Pulls the most recent trading day's grouped daily bars for ALL US stocks/ETFs in
 * a single call, ranks every ticker by dollar volume (price * volume) as a proxy for
 * market cap / liquidity, and writes the top N symbols back into Top1000Symbols.ts —
 * replacing the previously hand-maintained (and undersized, 707/1800) list with a
 * fresh, complete, live-data-backed universe.
 *
 * Usage: node scripts/generate-ticker-list.mjs [count]
 *   count defaults to 1800
 */

import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))

const API_KEY = process.env.POLYGON_API_KEY || 'kjZ4aLJbqHsEhWGOjWMBthMvwDLKd4wf'
const TARGET_COUNT = parseInt(process.argv[2] || '1800', 10)
const MIN_PRICE = 1 // exclude sub-$1 penny stocks
const OUTPUT_PATH = path.join(__dirname, '..', 'src', 'lib', 'Top1000Symbols.ts')

function toDateStr(d) {
  return `${d.getFullYear()}-${String(d.getMonth() + 1).padStart(2, '0')}-${String(d.getDate()).padStart(2, '0')}`
}

// Walk backwards from today until we find a date with actual grouped-bar results
// (handles weekends/holidays/today-not-yet-available).
async function fetchLatestGroupedBars() {
  let cursor = new Date()
  for (let attempt = 0; attempt < 10; attempt++) {
    const dateStr = toDateStr(cursor)
    const url = `https://api.polygon.io/v2/aggs/grouped/locale/us/market/stocks/${dateStr}?adjusted=true&apikey=${API_KEY}`
    console.log(`[generate-ticker-list] Trying ${dateStr}...`)
    const res = await fetch(url)
    if (res.ok) {
      const json = await res.json()
      if (Array.isArray(json.results) && json.results.length > 100) {
        console.log(`[generate-ticker-list] Got ${json.results.length} tickers for ${dateStr}`)
        return { dateStr, results: json.results }
      }
      console.log(`[generate-ticker-list] ${dateStr} had ${json.results?.length ?? 0} results - trying previous day`)
    } else {
      console.log(`[generate-ticker-list] ${dateStr} HTTP ${res.status} - trying previous day`)
    }
    cursor.setDate(cursor.getDate() - 1)
  }
  throw new Error('Could not find a trading day with grouped bar data in the last 10 days')
}

function isPlainTicker(ticker) {
  // Keep normal tickers and share classes like BRK.B; exclude warrants/units/rights (W, U, R suffixes
  // after a dot are common OTC/SPAC noise) and anything with weird characters.
  return /^[A-Z]{1,5}(\.[A-Z])?$/.test(ticker)
}

async function main() {
  const { dateStr, results } = await fetchLatestGroupedBars()

  const ranked = results
    .filter((r) => r.T && isPlainTicker(r.T) && r.c >= MIN_PRICE && r.v > 0)
    .map((r) => ({ ticker: r.T, dollarVolume: r.c * r.v }))
    .sort((a, b) => b.dollarVolume - a.dollarVolume)

  const topSymbols = ranked.slice(0, TARGET_COUNT).map((r) => r.ticker)

  console.log(`[generate-ticker-list] Ranked ${ranked.length} eligible tickers by $ volume on ${dateStr}`)
  console.log(`[generate-ticker-list] Taking top ${topSymbols.length} (requested ${TARGET_COUNT})`)

  const body = topSymbols.map((t) => `  '${t}',`).join('\n')

  const fileContent = `// TOP ${TARGET_COUNT} STOCKS/ETFS BY DOLLAR VOLUME (auto-generated)
// Source: Polygon /v2/aggs/grouped/locale/us/market/stocks/${dateStr}
// Regenerated by scripts/generate-ticker-list.mjs on ${new Date().toISOString()}
// Ranked by (close price * volume) on the most recent trading day as a liquidity/market-cap proxy.
// Run \`node scripts/generate-ticker-list.mjs [count]\` to refresh.

export const TOP_1800_SYMBOLS = [
${body}
]

// Organize by tiers for smart preloading
export const PRELOAD_TIERS = {
  TIER_1_INSTANT: TOP_1800_SYMBOLS.slice(0, 100), // Top 100 - preload every 5 minutes
  TIER_2_FAST: TOP_1800_SYMBOLS.slice(100, 300), // 101-300 - preload every 15 minutes
  TIER_3_REGULAR: TOP_1800_SYMBOLS.slice(300, 600), // 301-600 - preload every 30 minutes
  TIER_4_BACKGROUND: TOP_1800_SYMBOLS.slice(600, 1000), // 601-1000 - preload every 60 minutes
  TIER_5_EXTENDED: TOP_1800_SYMBOLS.slice(1000, 1400), // 1001-1400 - preload every 2 hours
  TIER_6_COMPREHENSIVE: TOP_1800_SYMBOLS.slice(1400, 1800), // 1401-1800+ - preload every 4 hours
}

export default TOP_1800_SYMBOLS

// Maintain backward compatibility
export const TOP_1000_SYMBOLS = TOP_1800_SYMBOLS
`

  fs.writeFileSync(OUTPUT_PATH, fileContent, 'utf8')
  console.log(`[generate-ticker-list] Wrote ${topSymbols.length} symbols to ${OUTPUT_PATH}`)
}

main().catch((err) => {
  console.error('[generate-ticker-list] FAILED:', err)
  process.exit(1)
})
