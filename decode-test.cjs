const rev = {};
for(const [b,u] of Object.entries(w1252)) rev[u]=parseInt(b);

function cpToByte(cp){ if(cp<0x80) return cp; if(rev[cp]!==undefined) return rev[cp]; if(cp>=0x80&&cp<=0xFF) return cp; return cp; }
function decode(cps){ const bytes=cps.map(cpToByte); return Buffer.from(bytes).toString('utf8'); }

